#include <iostream>
using namespace std;

int main(){
    string name;
    int age;
    
    cout<<"Enter your name and age: ";
    cin>>name>>age;
    
    cout<<"my name is: "<<name<<endl;
    cout<<"my age is: "<<age;
   
}
